double = lambda x:x*2
print(double(5))

multiply = lambda x,y:x*y
print(multiply(10,20))

add = lambda x,y,z:x+y+z
print(add(3,4,5))

fullname = lambda first_name,last_name:first_name +" "+last_name
print(fullname("Koya","Kalyan"))

age_check = lambda age:True if age >= 18 else False
print(age_check(17))